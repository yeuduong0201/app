  private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    // TODO add your handling code here:
    String content = textField.getText();
    if(content.equals("")) {
      JOptionPane.showMessageDialog(null, "Field can not be empty.");
    }
    else {
      listModel.addElement(content);
      JOptionPane.showMessageDialog(null, "add todo successfully.");
      Document doc = new Document("todo_work", content).append("user_id", todoId).append("created_at", LocalDate.now()).append("is_finished", false);
      Connection.addTodo(doc);
    }
    textField.setText(null);
  }//GEN-LAST:event_btnAddActionPerformed

