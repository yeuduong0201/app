  private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
    // TODO add your handling code here:
    String preValue = todoList.getSelectedValue();
    var content = JOptionPane.showInputDialog("update your todo", preValue);
    int index = todoList.getSelectedIndex();
    listModel.setElementAt(content, index);
    Connection.updateTodo(preValue, content);
  }//GEN-LAST:event_btnEditMouseClicked

