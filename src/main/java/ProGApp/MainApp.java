  private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    // TODO add your handling code here:
    int index = todoList.getSelectedIndex();
    String value = todoList.getSelectedValue();
    int reply = JOptionPane.showConfirmDialog(null, "Are you sure?", "Delete", JOptionPane.YES_NO_OPTION);
    if(reply == JOptionPane.YES_OPTION) {
      listModel.removeElementAt(index);
      JOptionPane.showMessageDialog(null, "Delete todo successfully");
      Connection.deleteTodo(value);
    }
    else {
      btnDelete.setEnabled(false);
      btnEdit.setEnabled(false);
      btnAdd.setEnabled(true);
    }
  }//GEN-LAST:event_btnDeleteActionPerformed

  private void btnFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishActionPerformed
    // TODO add your handling code here:
    int index = todoList.getSelectedIndex();
    if(index > -1) {  
      String value = todoList.getSelectedValue();
      listModel.removeElementAt(index);
      listDone.addElement(value);
      Connection.updateFinishTask(value);
    }
  }//GEN-LAST:event_btnFinishActionPerformed

